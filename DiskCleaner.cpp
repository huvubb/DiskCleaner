int main() {
    SetConsoleOutputCP(CP_UTF8);
    SetConsoleCP(CP_UTF8);

    ChooseLanguage();
    bool isAdmin = IsRunningAsAdmin();

    if (!ShowDisclaimer(isAdmin)) {
        std::cout << "\n" << g_lang->cancelled;
        std::cin.get();
        return 0;
    }

    wchar_t userProfile[MAX_PATH], localAppData[MAX_PATH];
    GetEnvironmentVariableW(L"USERPROFILE", userProfile, MAX_PATH);
    GetEnvironmentVariableW(L"LOCALAPPDATA", localAppData, MAX_PATH);
    std::wstring p(userProfile), l(localAppData);

    std::vector<CleanTarget> targets = {
        {g_lang->name_user_temp,       p + L"\\AppData\\Local\\Temp", true, false},
        {g_lang->name_chrome_cache,    l + L"\\Google\\Chrome\\User Data\\Default\\Cache", true, false},
        {g_lang->name_chrome_code,     l + L"\\Google\\Chrome\\User Data\\Default\\Code Cache", true, false},
        {g_lang->name_edge_cache,      l + L"\\Microsoft\\Edge\\User Data\\Default\\Cache", true, false},
        {g_lang->name_edge_code,       l + L"\\Microsoft\\Edge\\User Data\\Default\\Code Cache", true, false},
        {g_lang->name_firefox_cache,   l + L"\\Mozilla\\Firefox\\Profiles", true, false},
        {g_lang->name_thumb_cache,     p + L"\\AppData\\Local\\Microsoft\\Windows\\Explorer", true, false},
        {g_lang->name_win_temp,        L"C:\\Windows\\Temp", true, true},
        {g_lang->name_recycle,         L"C:\\$Recycle.Bin", true, true},
        {g_lang->name_prefetch,        L"C:\\Windows\\Prefetch", true, true},
        {g_lang->name_win_update,      L"C:\\Windows\\SoftwareDistribution\\Download", true, true},
        {g_lang->name_wer,             L"C:\\ProgramData\\Microsoft\\Windows\\WER", true, true},
        {g_lang->name_win_logs,        L"C:\\Windows\\Logs", true, true},
    };

    auto results = ScanAndReport(targets, isAdmin);

    std::set<int> sel;
    int mode = AskCleanMode(results, sel);
    if (mode == -1) {
        std::cout << "\n" << g_lang->cancelled;
        std::cin.get();
        return 0;
    }

    if (mode == 0)
        ExecuteClean(targets, isAdmin, nullptr, results);
    else
        ExecuteClean(targets, isAdmin, &sel, results);

    std::cout << "\n" << g_lang->press_enter;
    std::cin.get();
    return 0;
}
