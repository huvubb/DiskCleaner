std::string FormatSize(ULONGLONG bytes) {
    const char* units[] = { "B", "KB", "MB", "GB", "TB" };
    int idx = 0;
    double val = static_cast<double>(bytes);
    while (val >= 1024.0 && idx < 4) { val /= 1024.0; ++idx; }
    char buf[64];
    snprintf(buf, sizeof(buf), "%.2f %s", val, units[idx]);
    return buf;
}

const char* ClassifySize(ULONGLONG bytes) {
    if (bytes < 500ULL * 1024 * 1024)       return g_lang->level_light;
    if (bytes < 2ULL * 1024 * 1024 * 1024)  return g_lang->level_medium;
    if (bytes < 5ULL * 1024 * 1024 * 1024)  return g_lang->level_moderate;
    if (bytes < 10ULL * 1024 * 1024 * 1024) return g_lang->level_heavy;
    return                                       g_lang->level_severe;
}

bool IsRunningAsAdmin() {
    BOOL isAdmin = FALSE;
    HANDLE token = nullptr;
    if (OpenProcessToken(GetCurrentProcess(), TOKEN_QUERY, &token)) {
        TOKEN_ELEVATION elevation;
        DWORD size = sizeof(TOKEN_ELEVATION);
        if (GetTokenInformation(token, TokenElevation, &elevation, size, &size))
            isAdmin = elevation.TokenIsElevated;
        CloseHandle(token);
    }
    return isAdmin != FALSE;
}

ULONGLONG ScanFolder(const std::wstring& folder) {
    ULONGLONG total = 0;
    std::error_code ec;
    fs::path fp(folder);
    if (!fs::exists(fp, ec)) return 0;
    for (auto& entry : fs::recursive_directory_iterator(fp, ec)) {
        if (ec) { ec.clear(); continue; }
        if (fs::is_regular_file(entry, ec)) {
            ULONGLONG sz = fs::file_size(entry, ec);
            if (!ec) total += sz;
        }
    }
    return total;
}

ULONGLONG DeleteFolder(const std::wstring& folder) {
    ULONGLONG freed = 0;
    std::error_code ec;
    fs::path fp(folder);
    if (!fs::exists(fp, ec)) return 0;
    for (auto& entry : fs::recursive_directory_iterator(fp, ec)) {
        if (ec) { ec.clear(); continue; }
        if (fs::is_regular_file(entry, ec)) {
            ULONGLONG sz = fs::file_size(entry, ec);
            if (!ec) freed += sz;
            fs::remove(entry, ec);
            if (ec) g_totalFailed++;
        }
    }
    for (auto& entry : fs::recursive_directory_iterator(fp, ec)) {
        if (ec) { ec.clear(); continue; }
        fs::remove(entry, ec);
    }
    fs::remove(fp, ec);
    return freed;
}

